class AILoanRecommendation:
    def __init__(self):
        self.loans_db = GOVERNMENT_LOANS
        self.profiles_db = FARMER_PROFILES
    
    def get_loan_recommendations(self, user_id: str, language: str = "en") -> Dict:
        """
        Auto-fetch user profile and return eligible loans in selected language.
        
        Args:
            user_id: Logged-in farmer ID
            language: 'en', 'ta', or 'ne'
        
        Returns:
            Dict with farmer details and eligible loan recommendations
        """
        # Fetch farmer profile from database
        if user_id not in self.profiles_db:
            return {
                "status": "ERROR",
                "message": "User profile not found. Please complete your profile.",
                "user_id": user_id
            }
        
        farmer = self.profiles_db[user_id]
        state = farmer["state"]
        land_size = farmer["land_size"]
        
        # Get loans for farmer's state
        state_loans = self.loans_db.get(state, [])
        
        # Filter eligible loans based on land requirement
        eligible_loans = []
        for loan in state_loans:
            if land_size >= loan["min_land_required"]:
                # Format loan in selected language
                eligible_loans.append({
                    "loan_id": loan["loan_id"],
                    "loan_name": loan["loan_name"].get(language, loan["loan_name"]["en"]),
                    "eligible_farmer_type": loan["eligible_farmer_type"].get(language, loan["eligible_farmer_type"]["en"]),
                    "maximum_amount": loan["maximum_amount"],
                    "interest_rate": loan["interest_rate"].get(language, loan["interest_rate"]["en"]) if isinstance(loan["interest_rate"], dict) else loan["interest_rate"],
                    "description": loan["description"].get(language, loan["description"]["en"]),
                    "loan_image": loan["loan_image"],
                    "apply_link": loan["apply_link"],
                    "provider": loan["provider"]
                })
        
        return {
            "status": "SUCCESS",
            "user_id": user_id,
            "farmer_name": farmer["name"],
            "state": state,
            "district": farmer["district"],
            "land_size": f"{land_size} acres",
            "crop_type": farmer["crop_type"],
            "language": language,
            "total_eligible_loans": len(eligible_loans),
            "recommended_loans": eligible_loans,
            "generated_at": datetime.now().isoformat(),
            "visibility": "PRIVATE_USER_ONLY"
        }

